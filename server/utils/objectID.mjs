/**
 * *****************************************************************************
 *
 * object ID
 *
 * 12个字节
 *
 * * 0-3位为时间戳,
 * * 4-6为机器码,主机名称的散列值
 * * 7-8为PID,保证同一秒不同进程产生的id唯一
 * * 9-11为计数器,确保同进程同一秒产生的id 唯一
 *
 * exapmle:
 * 643922155741d4033387b635
 * 643921e760f523cd8641c138
 *
 * *****************************************************************************
 */

import mongodb from 'mongodb';

export const objectID = () => new mongodb.ObjectID();
