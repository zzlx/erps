/**
 * *****************************************************************************
 *
 * 混洗阵列
 *
 * 该函数用于打乱数组顺序
 *
 * *****************************************************************************
 */

export function shuffleSort (arr) {
  return arr.sort(() => 0.5 - Math.random());
}

