/**
 * *****************************************************************************
 *
 * Secure Hash Algorithm 1
 * ================
 *
 * SHA-1是一种密码散列函数，
 * 可以生成一个被称为消息摘要的160位（20字节）散列值，散列值通常的呈现形式为40个十六进制数。
 *
 * *****************************************************************************
 */

import { SHA1 } from './SHA1.mjs';

export function sha1 () {
  const msg = [...arguments].join('');
  return new SHA1().update(msg);
}
